def format_date_ru(day: int, month_idx: int) -> str:
    """
    Формирует строку вида "15 января" из дня и индекса месяца (1..12).

    Args:
        day: день месяца.
        month_idx: номер месяца 1..12.

    Returns:
        Строка "{день} {месяц_в_родительном}".
    """
    return f"{day} {MONTHS_RU[month_idx - 1]}"


def format_date_short_ru(date_str: str) -> str:
    """Форматирует YYYY-MM-DD → '15 янв'. Для компактных списков."""
    try:
        dt = datetime.strptime(date_str, "%Y-%m-%d")
        return f"{dt.day} {MONTHS_SHORT_RU[dt.month - 1]}"
    except ValueError:
        return date_str


